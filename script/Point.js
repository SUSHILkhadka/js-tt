class Point{
    constructor(x,y){
        this.x=x;
        this.y=y;
    }
    show(){
        console.log(this.x,this.y)
    }
}

class Point3D{
    constructor(x,y,z){
        this.x=x;
        this.y=y;
        this.z=z;
    }
    show(){
        console.log(this.x,this.y,this.z)
    }
}

class Plane3D{
    constructor(x,y,z,c){
        this.x=x;
        this.y=y;
        this.z=z;
        this.c=c;
    }
    show(){
        console.log(this.x,this.y,this.z,this.c)
    }
}


